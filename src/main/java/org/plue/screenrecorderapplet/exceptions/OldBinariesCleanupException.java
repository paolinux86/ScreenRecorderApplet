package org.plue.screenrecorderapplet.exceptions;

/**
 * @author paolo86@altervista.org
 */
public class OldBinariesCleanupException extends ScreenRecorderException
{
	public OldBinariesCleanupException()
	{
	}

	public OldBinariesCleanupException(String message)
	{
		super(message);
	}

	public OldBinariesCleanupException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public OldBinariesCleanupException(Throwable cause)
	{
		super(cause);
	}
}
